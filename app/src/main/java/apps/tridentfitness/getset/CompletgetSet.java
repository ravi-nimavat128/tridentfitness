package apps.tridentfitness.getset;

public class CompletgetSet {

    private String day, todays_date, month, workout_name, current_time, total_time, calories,total_exercise;

    public String getDay() {
        return day;
    }

    public void setDay(String day) {
        this.day = day;
    }

    public String getTodays_date() {
        return todays_date;
    }

    public String getWorkout_name() {
        return workout_name;
    }

    public void setWorkout_name(String workout_name) {
        this.workout_name = workout_name;
    }

    public String getCurrent_time() {
        return current_time;
    }

    public void setCurrent_time(String current_time) {
        this.current_time = current_time;
    }

    public String getTotal_time() {
        return total_time;
    }

    public void setTotal_time(String total_time) {
        this.total_time = total_time;
    }

    public String getCalories() {
        return calories;
    }

    public void setCalories(String calories) {
        this.calories = calories;
    }

    public void setTodays_date(String todays_date) {
        this.todays_date = todays_date;
    }

    public String getMonth() {
        return month;
    }

    public void setMonth(String month) {
        this.month = month;
    }

    public String getTotal_exercise() {
        return total_exercise;
    }

    public void setTotal_exercise(String total_exercise) {
        this.total_exercise = total_exercise;
    }
}
